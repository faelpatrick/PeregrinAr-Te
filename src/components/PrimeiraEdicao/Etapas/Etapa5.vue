            <h4>O QUARTO REI MAGO</h4>
            <p>
                Desde que me conheço que os tempos de Natal exercem sobre mim um forte
                fascínio. Em criança ficava horas sem conta em frente do enorme estrado onde
                os meus irmãos montavam o Presépio. Musgo a rodos tinha sido descascado
                dos valados para servir de tapete a tão sedutor cenário.
                <br>
                Nos meus tempos de infância feliz não havia árvore de Natal. Foi moda que
                veio mais tarde. O centro das minhas atenções era o Presépio. Nas minhas
                meditações na frente dele, lembro-me como tudo me maravilhava e a de maior
                volume era, como ainda hoje, a daqueles três Reis. Porquê três? Porquê
                Reis? E porquê Magos?
                <br>
                (sabia lá eu o que isso era!). E, de tão longe, vieram sozinhos ou com a sua
                corte? E dormiram no caminho? Onde? Como? Quantas noites? Chegaram
                no dia do Nascimento, ou antes ou depois? Então os reis, naquele tempo,
                davam-se ao luxo de abandonar seus países, deixá-los ao deus-dará, e ir
                em aventuras por montes e vales, só porque viram ou ouviram uma estrela a
                chamá-los? Desta vez deu certo, mas seria sempre assim? Reis do Oriente...
                Onde ficariam esses lugares? E um deles era preto, cá está ele, é o Gaspar!
                Mas os pretos são de África. Ficaria a África no Oriente? E sempre, sempre,
                teimosamente, este fascínio por tão ilustres e bizarras personagens.
                <br>
                Recentemente, encontrei um livro interessantíssimo, “O Livro dos Reis
                Magos”, da autoria de João Hildesheim, um monge carmelita, homem de
                grande cultura, que viveu um pouco por toda a Europa. Durante o pontificado
                de Clemente VI (1342-1352), viveu em Avinhão. Livro escrito, portanto, por
                meados do século XIV e um relato fascinante, sempre com recurso aos textos
                sagrados, sobre esses Três Reis, as suas viagens e a sua motivação.
                <br>
                Com a inclinação que sempre tive para saltar da realidade e meter-me pelos
                atalhos da fantasia até onde a imaginação me levar, neste extraordinário
                relato encontrei matéria que me causou “espécie”e deu início à história que
                vou narrar. Tratará ela da existência de um quarto Rei Mago vindo de outros
                lugares que não os da Núbia, da Pérsia, da Caldeia. Outros lugares, enfim,
                bem mais próximos destes onde nasci. Desde criança lamento que destas
                nossas terras não tivesse havido um rei ou alguma pessoa importante para
                nos representar e dar as boas-vindas a Jesus.
                <br>
                Decidida a escutar a minha imaginação, concluí que a dita estrela também
                não gostou da ideia de levar ao Menino apenas três adoradores. “É pouco –
                pensou ela – afinal trata-se do nascimento do Deus único e verdadeiro. Se ao
                menos encontrasse mais um...
                <br>
                Com esta ideia em mente, um dia, lá abandonou o seu posto cimeiro. E muito
                depressa, porque o tempo escasseava, lá correu a estrelinha até perto de
                nós. Espreitou a Europa toda e achou-a demasiado belicosa e pouco simpática. Pelo menos assim lhe
                pareceu. Aproximou-se mais. De repente viu um
                enorme oceano vivo e buliçoso, azul, azul, muito azul e transparente.
                <br>
                – Aqui, aqui tem que haver um Rei bom, generoso e disponível para ser o
                meu quarto Rei Mago, para se juntar aos outros e adorar o Menino – dizia ela,
                confiante.
                <br>
                Junto de enorme areal que o mar beijava amorosamente, avistou um jovem. O
                mais belo jovem que jamais conhecera. Cabelo loiro como o sol, olhos azuis
                como o mar, corpo doirado como a areia. Foi até ele, fascinada com tanta
                beleza. Num passe de mágica de que só as estrelas são capazes, transformou-se a estrelinha numa bela
                mulher que, sorrindo, interpelou o homem.
                <br>– Que fazes aqui, tão sozinho, neste grande areal, olhando o oceano?
                <br>– Sonho – respondeu o jovem sem deixar de fitar o mar.
                <br>– Sonhas? Então quem és tu?
                <br>– Eu sou um sonhador, portanto, um poeta.
                <br>– Mas... E que fazes além de sonhar? Isso de ser poeta, afinal, o que é?
                <br>– Além de sonhar, não faço mais nada, por enquanto. Isso de ser poeta é tão
