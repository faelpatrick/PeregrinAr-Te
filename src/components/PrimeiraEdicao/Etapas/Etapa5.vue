                mais nada. E tu, linda jovem, quem és? Venho aqui todos os dias e nunca te
                encontrei.
                <br>– Se calhar... Se calhar sou a tua alma gémea e fico feliz por te ter encontrado.
                Mas diz-me, por acaso tu não serás rei?
                <br>– Claro que sou rei! – responde o homem com rapidez.
                <br>– Não imaginas quanto me alegra a tua resposta. Era em busca de um rei que
